/// Build a training corpus that includes dialogue patterns merged with medium_corpus.
/// Includes both English and Korean dialogue data.
pub fn chat_corpus() -> Vec<&'static str> {
    let mut corpus = Vec::new();
    for intent in chat_intents_ko() {
        for p in intent.patterns {
            corpus.push(p);
        }
        for r in intent.responses {
            corpus.push(r);
        }
    }
    corpus
}

/// Build labeled classification data from Korean dialogue intents.
///
/// Returns (labeled_pairs, class_labels) suitable for `train_classifier()`.
pub fn chat_classification_data() -> (Vec<(&'static str, usize)>, Vec<String>) {
    let intents = chat_intents_ko();
    let class_labels: Vec<String> = intents.iter().map(|i| i.intent.clone()).collect();
    let mut labeled = Vec::new();
    for (idx, intent) in intents.iter().enumerate() {
        for &pattern in &intent.patterns {
            labeled.push((pattern, idx));
        }
    }
    (labeled, class_labels)
}

/// Corpus statistics.
pub struct CorpusStats {
    pub num_sentences: usize,
    pub num_words: usize,
    pub unique_words: usize,
    pub avg_sentence_len: f64,
    pub max_sentence_len: usize,
}

impl CorpusStats {
    pub fn from_corpus(sentences: &[&str]) -> Self {
        let num_sentences = sentences.len();
        let mut total_words = 0;
        let mut max_len = 0;
        let mut unique = std::collections::HashSet::new();

        for s in sentences {
            let words: Vec<&str> = s.split_whitespace().collect();
            total_words += words.len();
            if words.len() > max_len {
                max_len = words.len();
            }
            for w in &words {
                unique.insert(w.to_lowercase());
            }
        }

        CorpusStats {
            num_sentences,
            num_words: total_words,
            unique_words: unique.len(),
            avg_sentence_len: total_words as f64 / num_sentences.max(1) as f64,
            max_sentence_len: max_len,
        }
    }

    pub fn print(&self) {
        println!("  Sentences:        {}", self.num_sentences);
        println!("  Total words:      {}", self.num_words);
        println!("  Unique words:     {}", self.unique_words);
        println!("  Avg sentence len: {:.1}", self.avg_sentence_len);
        println!("  Max sentence len: {}", self.max_sentence_len);
    }
}
